// دالة للحصول على سؤال عشوائي
export const getRandomRiskQuestion = () => {
  const randomIndex = Math.floor(Math.random() * riskQuestions.length);
  return riskQuestions[randomIndex];
};

// دالة للحصول على سؤال حسب الصعوبة
export const getRiskQuestionByDifficulty = (difficulty) => {
  const filteredQuestions = riskQuestions.filter(q => q.difficulty === difficulty);
  if (filteredQuestions.length === 0) return null;
  
  const randomIndex = Math.floor(Math.random() * filteredQuestions.length);
  return filteredQuestions[randomIndex];
};

// دالة للحصول على سؤال حسب الفئة
export const getRiskQuestionByCategory = (category) => {
  const filteredQuestions = riskQuestions.filter(q => q.category === category);
  if (filteredQuestions.length === 0) return null;
  
  const randomIndex = Math.floor(Math.random() * filteredQuestions.length);
  return filteredQuestions[randomIndex];
};

// الإحصائيات
export const getRiskQuestionsStats = () => {
  const stats = {
    total: riskQuestions.length,
    easy: riskQuestions.filter(q => q.difficulty === 'easy').length,
    medium: riskQuestions.filter(q => q.difficulty === 'medium').length,
    hard: riskQuestions.filter(q => q.difficulty === 'hard').length,
    categories: {}
  };
  
  riskQuestions.forEach(q => {
    if (!stats.categories[q.category]) {
      stats.categories[q.category] = 0;
    }
    stats.categories[q.category]++;
  });
  
  return stats;
};