// دالة للحصول على سؤال عشوائي من فئة معينة
export const getRandomQuestionFromCategory = (category) => {
  const questions = auctionGameData[category];
  if (!questions || questions.length === 0) return null;
  
  const randomIndex = Math.floor(Math.random() * questions.length);
  return questions[randomIndex];
};

// دالة للحصول على جميع الفئات
export const getAllCategories = () => {
  return Object.keys(auctionGameData);
};

// دالة للحصول على عدد الأسئلة لكل فئة
export const getCategoryStats = () => {
  const stats = {};
  Object.keys(auctionGameData).forEach(category => {
    stats[category] = auctionGameData[category].length;
  });
  return stats;
};